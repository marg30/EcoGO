package entidades;

public class Barrio {
    private int _id;
    private String _nombre;
    private Comuna _comuna;
    
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public String get_nombre() {
		return _nombre;
	}
	public void set_nombre(String _nombre) {
		this._nombre = _nombre;
	}
	public Comuna get_comuna() {
		return _comuna;
	}
	public void set_comuna(Comuna _comuna) {
		this._comuna = _comuna;
	}
}
    
    
    